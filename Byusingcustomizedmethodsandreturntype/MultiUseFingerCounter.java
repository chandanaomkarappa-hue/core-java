class MultiUseFingerCounter
{
	public static String getDepartment()
	{
		System.out.println("getDepartment invoked");
		System.out.println("Unisex-Adult");
		return "Unisex-Adult";
	}

	public static String getManufacturer()
	{
		System.out.println("getManufacturer invoked");
		System.out.println("Arthav by sanwariya Group");
		return "Arthav by sanwariya Group";
	}

	public static String getCountryOfOrigin()
	{
		System.out.println("getCountryOfOrigin invoked");
		System.out.println("India");
		return "India";
	}

	public static String getAsin()
	{
		System.out.println("getAsin invoked");
		System.out.println("B0GBWPDCBX");
		return "B0GBWPDCBX";
	}

	public static String getItemWeight()
	{
		System.out.println("getItemWeight invoked");
		System.out.println("10 g");
		return "10 g";
	}
}